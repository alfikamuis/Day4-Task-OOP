package Soal.Calculator;

public class Operations {

    public static int addOperation(int value1, int value2){
        int result = value1+value2;
        return result;
    }

    public static int subOperation(int value1, int value2){
        int result = value1-value2;
        return result;
    }

    public static int multiplyOperation(int value1, int value2){
        int result = value1*value2;
        return result;
    }
    public static int divideOperation(int value1, int value2){
        int result = value1/value2;
        return result;
    }
}
